import unittest

from InfiniteDigitalString.main import find_position

class MyTestCase(unittest.TestCase):
    def test_something(self):
        test_cases = [
            ("456", 3, "...3456..."),
            ("454", 79, "...444546..."),
            ("455", 98, "...545556..."),
            ("910", 8, "...7891011..."),
            ("9100", 188, "...9899100..."),
            ("99100", 187, "...9899100..."),
            ("00101", 190, "...9899100..."),
            ("001", 190, "...9899100..."),
            ("00", 190, "...9899100..."),
            ("123456789", 0),
            ("1234567891", 0),
            ("123456798", 1000000071),
            ("10", 9),
            ("53635", 13034),
            ("040", 1091),
            ("11", 11),
            ("99", 168),
            ("667", 122),
            ("0404", 15050),
            ("949225100", 382689688),
            ("58257860625", 24674951477),
            ("3999589058124", 6957586376885),
            ("555899959741198", 1686722738828503),
            ("01", 10),
            ("091", 170),
            ("0910", 2927),
            ("0991", 2617),
            ("09910", 2617),
            ("09991", 35286),
        ]

        for num, *ans in test_cases:
            result = find_position(num)
            self.assertEqual(result, *ans)
            # print(ans)


if __name__ == '__main__':
    unittest.main()
